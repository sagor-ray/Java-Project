    public void OperatorAction()
    {
       
         secondNum=Double.parseDouble(scientifictextfield.getText());
        
        
        switch(Operation)
        {
            case "+":
            {
                result=firstNum+secondNum;
                scientifictextfield.setText(String.valueOf(result));
                break;
            }
            case "-":
            {
                result=firstNum-secondNum;
                scientifictextfield.setText(String.valueOf(result));
                break;
            }
            case "*":
            {
                result=firstNum*secondNum;
                scientifictextfield.setText(String.valueOf(result));
                break;
            }
            case "/":
            {
                result=firstNum/secondNum;
                scientifictextfield.setText(String.valueOf(result));
                break;
            }
            case "x^y":
            {
               double value=1;
              
                for(int i=1;i<=secondNum;i++)
                {
                    value=firstNum*value;
                }
                scientifictextfield.setText(String.valueOf(value));
                break;
            }
        }
    }
